public class FunctionWithPara {

    public static void main(String[] args) {
        System.out.println("enter");
        // function call
        addition();
        System.out.println("exit");
    }

    // function definition
    public static void addition() {

        int a = 6;
        int b = 8;

        int c = a + b;
        System.out.println(c);
    }

}
